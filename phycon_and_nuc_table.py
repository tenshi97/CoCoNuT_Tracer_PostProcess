#-----------------------------------------------------------------------

#      real   pc_meverg ! transformation from MeV to erg
pc_meverg = 1.602e-6

#-----------------------------------------------------------------------

#      real   pc_ergmev ! transformation from erg to MeV
pc_ergmev = 1.0/pc_meverg
#                 = 6.242197e5
#-----------------------------------------------------------------------

#      real   pc_mevk   ! transformation from MeV to K
pc_mevk = pc_meverg/pc_kb
#                 = 1.16032e10
#-----------------------------------------------------------------------

#      real   pc_kmev   ! transformation from K to MeV
pc_kmev = 1.0/pc_mevk
#                 = 8.61829e-11
#-----------------------------------------------------------------------


#-----------------------------------------------------------------------
# weak interaction constants
#-----------------------------------------------------------------------

#real wc_s2w         !sin^2(theta_W)
wc_s2w = 0.23    

#real wc_ca,wc_cv
