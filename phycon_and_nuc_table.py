#real wc_hc          !hquer*c [MeV*cm]
wc_hc = 197.327e-13  

#real wc_me          !electron rest energy [MeV]
wc_me = pc_me*pc_cl*pc_cl*pc_ergmev

#real wc_mp          !proton rest energy [MeV]
wc_mp = pc_mp*pc_cl*pc_cl*pc_ergmev

#real wc_mn          !neuton rest energy [MeV]
wc_mn = pc_mn*pc_cl*pc_cl*pc_ergmev

#real wc_mb          !baryon rest energy [MeV]
wc_mb = pc_mb*pc_cl*pc_cl*pc_ergmev

#real wc_mq          !rest-energy difference n-p [MeV]
wc_mq = (pc_mn-pc_mp)*pc_cl*pc_cl*pc_ergmev 


