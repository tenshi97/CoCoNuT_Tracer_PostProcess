#-----------------------------------------------------------------------

#      real   pc_kb     ! Boltzmann constant [erg/K]
pc_kb = 1.380658e-16

#-----------------------------------------------------------------------

#      real   pc_mb     ! baryon mass [g]
pc_mb     = 1.66e-24 

#-----------------------------------------------------------------------

#      real   pc_mn     ! neutron mass [g]
